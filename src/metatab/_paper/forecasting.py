"""
Program to generate a forecasting learning curve on a dataset using 2 modelling approach (main + baseline).
Namely a modelling approach is an estimator plus training regime (default, (meta)tune, (meta)ensemble).
The curves are generated by splitting the data in train + test, and the train set in multiple set with 
increasing size. The models are fitted on the train set and tested on the test one.

Since the program is not intended to be used by users it misses some degree of freedom. In particular:
- we enforce a set of 2 estimators. So is not possible to perform the analysis on just 1 or more than 2.
- we enforce a fixed set of subtrain split, aka the split ratios applied to the train set.
- we use the default setting for the meta estimators in terms of candidate points and meta seed (no parameters to control this).
- we enforce the "default settings" for each regime, where for default we mean the ones explored in the original paper.
- we enforce the "df" dataset mode for loading the datasets

Note: to have full freedom one can use the metatab fit/predict API. 
In this case we must generate the datasets split beforehand. 
The we use metatab API to fit and predict calling the programs multiple times.
"""

